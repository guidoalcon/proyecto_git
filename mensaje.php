			<p>
				
Esta es una pregunta q muchos se han hecho, quizá para debatir, justificarse o porq tienen una preocupación sobre su rol dentro de la misma, muchos otros también ya han perdido la verdadera perspectiva de las funciones de la iglesia, y otros tantos aún han menospreciado a causa de malas experiencias la importancia de pertenecer a una congregación de creyentes en Cristo y por lo tanto el congregarse aún como tales.
♡|Hechos 2:42|♡ puede ser considerado como una declaración del propósito de la iglesia, “Y perseveraban en la doctrina de los apóstoles, en la comunión unos con otros, en el partimiento del pan y las oraciones.” Así q, de acuerdo a esta Escritura, el propósito o actividades de la iglesia deben ser; </p>
<ul>
	<li>(1) Enseñar la doctrina bíblica, </li>
	<li>(2) proveer un lugar de convivencia para los creyentes, </li>
	<li>(3) celebrar la Cena del Señor, y </li>
	<li>(4) orar.</li>
</ul>
<p>
La iglesia está para enseñar la doctrina Bíblica, a fin de q podamos ser arraigados en nuestra fe. ♡|Efesios 4:14|♡ nos dice, “para que ya no seamos niños fluctuantes, llevados por doquiera de todo viento de doctrina, por estratagemas de hombres que para engañar emplean con astucia las artimañas del error.” La iglesia está para ser un lugar de compañerismo, donde los cristianos puedan convivir fraternalmente y honrarse unos a otros ♡|Romanos 12:10|♡, instruirse unos a otros ♡|Romanos 15:14|♡, ser benignos y misericordiosos unos con otros ♡|Efesios 4:32|♡, animarse unos a otros ♡|1 Tesalonicenses 5:11|♡, y lo más importante, amarse unos a otros ♡|1 Juan 3:11|♡.
</p>
<p>
La iglesia está para ser un lugar donde los creyentes puedan celebrar la Cena del Señor, recordando la muerte de Cristo y Su sangre derramada por nosotros ♡|1 Corintios 11:23-26|♡....El concepto de “partir el pan” ♡|Hechos 2:42|♡ también conlleva la idea de comer juntos. Este es otro ejemplo del compañerismo promovido por la iglesia...El propósito final de la iglesia, de acuerdo a ♡|Hechos 2:42|♡ es orar. La iglesia es un lugar que promueve la oración, enseña la oración, y practica la oración. ♡|Filipenses 4:6-7|♡ nos anima a hacerlo, “Por nada estéis afanosos, sino sean conocidas vuestras peticiones delante de Dios en toda oración y ruego, con acción de gracias. Y la paz de Dios, que sobrepasa todo entendimiento, guardará vuestros corazones y vuestros pensamientos en Cristo Jesús.”
</p>
<p>
Otra “comisión” dada a la iglesia es el proclamar el Evangelio para la Salvación, a través de Jesucristo ♡|Mateo 28:18-20; Hechos 1:8|♡. La iglesia es llamada a compartir fielmente el Evangelio a través de su palabra y hechos....La iglesia está para ser un “faro de luz” en la comunidad – guiando a la gente hacia nuestro Señor y Salvador Jesucristo....La iglesia está tanto para promover el Evangelio como para preparar a sus miembros a proclamarlo ♡|1 Pedro 3:15|♡.
</p>
<p>
Algunos propósitos finales de la iglesia son dados en ♡|Santiago 1:27|♡, donde leemos “La religión pura y sin mácula delante de Dios el Padre es esta: Visitar a los huérfanos y a las viudas en sus tribulaciones, y guardarse sin mancha del mundo.” La iglesia está para ministrar a aquellos que están en necesidad. Esto incluye no sólo el compartir el Evangelio, sino también proveer para sus necesidades físicas |comida, ropa, hospedaje| como sea necesario y apropiado...La iglesia está también para equipar a los creyentes en Cristo con las herramientas q ellos necesitan para vencer al pecado y permanecer libres de la contaminación del mundo....Esto es logrado por los principios dados anteriormente – enseñanza bíblica y compañerismo cristiano.
</p>
<p>
Así q, con todo lo q aquí está dicho, ¿cuál es el propósito de la iglesia? Me gusta la ilustración en ♡|1 Corintios 12:12-27|♡. La iglesia es el “cuerpo” de Dios – somos Sus manos, boca y pies en este mundo....Estamos para hacer las cosas q Jesucristo haría si Él estuviera aquí físicamente en el mundo....La iglesia está para ser “cristiana” — es decir, “como Cristo” — y para ser seguidores de Cristo.
G.Q
